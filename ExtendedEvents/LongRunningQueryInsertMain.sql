[event_name]
,[package_name]
,[utc_timestamp]
,CONVERT(DATETIME, CONVERT(DATETIMEOFFSET, ([utc_timestamp] at time zone 'UTC') AT TIME ZONE 'Pacific Standard Time')) DatetimeLocal
