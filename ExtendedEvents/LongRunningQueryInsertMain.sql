([EventName]
,[PackageName]
,[DatetimeStampUTC]
,[DatetimeStampLocal]
,[EventDate]
,[ClientAppName]
,[ClientHostname]
,[Duration]
,[CPU]
,[LogicalReads]
,[PhysicalReads]
,[Writes]
